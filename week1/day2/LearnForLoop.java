package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {

		int value=10;
		
		//for -> press ctrl+space => 2nd option
		for (int i = 0; i < value; i++) {
			System.out.println(i);
		}
		
		for (int i = 10; i > 0; i--) {
			System.out.println(i);
		}
	}

}
